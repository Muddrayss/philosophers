#include "philosophers.h"

int	ft_atoi(const char *nptr)
{
	int		i;
	int		is_neg;
	long	num;

	i = 0;
	is_neg = 1;
	num = 0;
	while ((nptr[i] >= 9 && nptr[i] <= 13) || nptr[i] == ' ')
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			is_neg *= -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		num *= 10;
		num += nptr[i] - 48;
		i++;
	}
	return ((int)(num * is_neg));
}
